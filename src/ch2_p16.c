#include <stdio.h>

int main(void) {
  float x = 5 / 2; // Ακέραια διαίρεση
  printf("%.1f\n", x);
  x = (float)5 / (float)2; // Διαίρεση με αποτέλεσμα πραγματικό
  printf("%.1f\n", x);
  x = 5. / 2.; // Το ίδιο αποτέλεσμα μπορεί να επιτευχθεί διαιρώντας
               // πραγματικούς αριθμούς
  printf("%.1f\n", x);
  return 0;
}
